    public void upDateDB(){
           try{
                Class.forName("com.mysql.jdbc.Driver");
                sqlCon = DriverManager.getConnection(dataConn,username, password);
                pst = sqlCon.prepareStatement("SELECT vente.id, article.refpro, qtevendu, datevendu, nom, prenom\n" +
"FROM client, article, vente\n" +
"WHERE client.id = vente.id_client AND article.id = vente.id_article");
                
                rs = pst.executeQuery();
                ResultSetMetaData stData = rs.getMetaData();
                
                q = stData.getColumnCount();
                
                DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
                        RecordTable.setRowCount(0);
                        
                 while(rs.next()){
                     Vector columnData = new Vector();
                     
                     for(i=0; i<q; i++){
                         columnData.add(rs.getString("id"));
                         columnData.add(rs.getString("refpro"));
                         columnData.add(rs.getString("qtevendu"));
                         columnData.add(rs.getString("datevendu"));
                         columnData.add(rs.getString("nom")+" "+rs.getString("prenom"));
                     }
                     RecordTable.addRow(columnData);
                 }
           }catch(Exception ex){
               JOptionPane.showMessageDialog(null, ex);
           }
       }
    
