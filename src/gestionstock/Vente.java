    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String datev = datevente.getText().toString();
        String qtev = qte.getText().toString();
        int idcli = idclient.getSelectedIndex();
        int idart = idarticle.getSelectedIndex();
        
        String sql1 = "select * from client";
        String sql2 = "select * from article";
        try{
            Class.forName("com.mysql.jdbc.Driver");
            sqlCon = DriverManager.getConnection(dataConn,username, password);
            pst = sqlCon.prepareStatement(sql1);
            pst1 = sqlCon.prepareStatement(sql2);
            rs = pst.executeQuery();
            rs1 = pst1.executeQuery();
            int t = 0, y = 0;
            while(rs.next()){
                if(t ==(idcli-1)){
                    id_client_d = rs.getString("id");
                }
                t++;
            }
            
            while(rs1.next()){
                if(y ==(idart-1)){
                    id_article_d = rs1.getString("id");
                }
                y++;
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        
        
        if( idcli == 0 || idart == 0){
            JOptionPane.showMessageDialog(this, "Veillez-rempli les champs");
        }else{
            try{
                Class.forName("com.mysql.jdbc.Driver");
                sqlCon = DriverManager.getConnection(dataConn,username, password);
                pst = sqlCon.prepareStatement("insert into vente(id_article, id_client, qtevendu, datevendu) values(?,?,?,?)");
                pst.setString(1, id_article_d);
                pst.setString(2, id_client_d);
                pst.setString(3, qtev);
                pst.setString(4, datev);
                
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Enregistrement valide");
                upDateDB();
                
            } catch (ClassNotFoundException ex) {
                java.util.logging.Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
            }catch(SQLException ex){
                java.util.logging.Logger.getLogger(Client.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
        
        
        //JOptionPane.showMessageDialog(null, id_client_d);
        //JOptionPane.showMessageDialog(null, id_article_d);
    }//GEN-LAST:event_jButton2ActionPerformed

